export function DashboardNav() {
  const pathname = usePathname();

  function isActive(href: string) {
    if (href === "/dashboard") return pathname === "/dashboard";
    return pathname === href || pathname.startsWith(href + "/");
  }

  return (
    <aside className="fixed left-0 top-0 z-10 flex h-screen w-56 flex-col p-3">
      <div
        className="flex flex-1 flex-col rounded-2xl shadow-lg"
        style={{
          background: "linear-gradient(180deg, #2c2c2c 0%, #1f1f1f 100%)",
        }}
      >
        <div className="flex h-14 items-center px-5">
          <span className="font-semibold text-white/95">Cloud Admin</span>
        </div>
        <nav className="flex-1 space-y-1 overflow-y-auto px-2 py-3">
          {nav.map(({ href, label, icon: Icon }) => {
            const active = isActive(href);
            return (
              <Link
                key={href}
                href={href}
                className={`flex items-center gap-3 rounded-xl px-3 py-2.5 text-sm transition-colors ${
                  active
                    ? "bg-emerald-500/30 text-white"
                    : "text-white/75 hover:bg-white/10 hover:text-white"
                }`}
              >
                <Icon className="h-5 w-5 flex-shrink-0" />
                <span>{label}</span>
              </Link>
            );
          })}
        </nav>
        <div className="border-t border-white/10 px-2 py-3">
          <button
            type="button"
            onClick={() => {
              clearToken();
              window.location.href = "/login";
            }}
            className="flex w-full items-center gap-3 rounded-xl px-3 py-2.5 text-left text-sm text-white/75 transition-colors hover:bg-white/10 hover:text-white"
          >
            <IconLogout className="h-5 w-5 flex-shrink-0" />
            <span>Logout</span>
          </button>
        </div>
      </div>
    </aside>
  );
}
